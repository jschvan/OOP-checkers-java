package cpsc2150.extendedCheckers.util;

/**
 * The enumeration DirectionEnum represents the direction in which a piece on the checkerboard can move
 */

public enum DirectionEnum
{
    /**
     * Represents moving toward the top right corner.
     */
    NE,
    
    /**
     * Represents moving toward the top left corner.
     */
    NW,
    
    /**
     * Represents moving toward the bottom right corner.
     */
    SE,
    
    /**
     * Represents moving toward the bottom left corner.
     */
    SW
}
